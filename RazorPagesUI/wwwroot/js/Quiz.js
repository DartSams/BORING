function updateQuestion() {
    if (currentQuestion < QUESTIONS.length) {
        document.querySelector(".question").innerText = QUESTIONS[currentQuestion].question
        generateChoices(QUESTIONS[currentQuestion].labels, QUESTIONS[currentQuestion].type)
    } else {
        let choiceBody = document.querySelector(".question-choices")
        choiceBody.innerHTML = ""
        let footerContainer = document.querySelector("#footer-container")
        footerContainer.innerHTML = ""
        document.querySelector(".question").innerText = "You have reached the end of the survey."
    } // handles case for if the currentQuestion variable number is less than the max number of items in QUESTIONS
}

function generateChoices(choices,questionType) {
    let choiceBody = document.querySelector(".question-choices")
    //if (choices.length <= 3) {
       // choiceBody.style.cssText = `
            //display:flex;
            //flex-direction:column;
            //align-items:baseline;
            //flex-basis:50px;
            //width: 100%;
       // `
    //} else if (choices.length >= 7) {
    //    choiceBody.style.cssText = `
    //        display:flex;
     //       flex-direction:row;
     //       align-items:baseline;
    //    `
   // }
    choiceBody.innerHTML = ""

    for (let i = 0; i < choices.length;i++) {
        let choice = document.createElement("div"); 
        choice.className = "choice"

        let input = document.createElement("input");
        input.name = "choice"
        if (questionType == "single") {
            input.type = "radio"
        } else if (questionType == "multiple-choice") {
            input.type = "checkbox"
        }
        input.value = choices[i]

        let label = document.createElement("label");
        label.innerText = choices[i]

        choice.append(input)
        choice.append(label)
        choiceBody.append(choice)
    }
} //arguments are 1. a list of choices for a question 2. the type of answer expected for the question radio if single answer but checkbox to multiple-choice

